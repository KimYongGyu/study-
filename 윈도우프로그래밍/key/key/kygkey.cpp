void CAboutDlg::OnKeyDown(UINT nChar, UINT nRepCnt, UINT nFlags)
{
	// TODO: 여기에 메시지 처리기 코드를 추가 및/또는 기본값을 호출합니다.
	switch(nChar) {
		//방향키 왼쪽
		case VK_LEFT :  
			m_xPos -= 20;
			break;

			//방향키 오른쪽
		case VK_RIGHT:
			m_xPos += 20;
			break;

			//방향키 위쪽
		case VK_UP:
			m_yPos += 20;
			break;

			//방향키 아래쪽
		case VK_DOWN:
			m_yPos -= 20;
			break;
	}
	Invalidate();
	// OnPaint 로 이동
				
	CDialogEx::OnKeyDown(nChar, nRepCnt, nFlags);
}




